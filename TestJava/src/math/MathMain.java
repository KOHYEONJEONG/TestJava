package math;

public class MathMain {

	public static void main(String[] args) {
		//수학 함수
		float pi = 3.1415f;//f붙히기
		
		System.out.println(Math.ceil(pi));//올림
		System.out.println(Math.floor(pi));//버림
		System.out.println(Math.round(pi));//반올림
		
		
		System.out.println(Math.abs(3.14));//절대값

	}

}
